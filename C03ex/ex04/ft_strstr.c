char	*ft_strstr(char *str, char *to_find)
{
	int i;

	i = 0;
	while (*to_find != '\0')
	{
		if (*str == *to_find)
		{
			str++;
			to_find++;
		}
		else
		{
			str++;
		}
	}
	return (0);
}
